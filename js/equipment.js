// Convert a category or list of categories into a human‚Äëreadable label.
// Items may now belong to multiple categories. When an array is passed
// the labels are joined with commas. Unknown categories fall back to
// their raw names.
function categoryLabel(cat) {
  const m = { chemistry: "Chemistry", biology: "Biology", physics: "Physics" };
  if (Array.isArray(cat)) {
    return cat
      .map(c => m[c] || c)
      .filter(Boolean)
      .join(", ");
  }
  return m[cat] || (cat || "Mixed");
}

// Choose an emoji to represent an equipment item. Legacy IDs map to
// special emojis for backwards compatibility. When multiple categories
// are provided the first category determines the icon.
function emojiFor(cat, id) {
  if (id === "microscope") return "üî¨";
  if (id === "watch-glass") return "üß´";
  if (id === "slide-cover-slip") return "üß™";
  // if categories is an array, use the first entry
  const c = Array.isArray(cat) ? cat[0] : cat;
  if (c === "physics") return "‚ö°";
  if (c === "biology") return "üß¨";
  return "üß™";
}

function renderCard(item) {
  const name = `${item.name_bn} <span class="muted small">(${item.name_en})</span>`;
  // Determine thumbnail: prefer an image when provided
  let thumbHtml;
  if (item.image) {
    // Use escapeHtml if available, otherwise fall back to the raw name
    const altText = typeof escapeHtml === 'function' ? escapeHtml(item.name_en) : (item.name_en || "");
    thumbHtml = `<img src="${item.image}" alt="${altText}" class="thumb-img" />`;
  } else {
    // fall back to emoji; categories may be array or string
    thumbHtml = emojiFor(item.categories || item.category, item.id);
  }
  // Build individual category pills for the equipment library. When a tool
  // belongs to multiple categories, create a separate small tag for each.
  const cats = Array.isArray(item.categories)
    ? item.categories
    : (item.category ? [item.category] : []);
  const tagsHtml = cats
    .map(cat => `<span class="cat-tag">${categoryLabel(cat)}</span>`) // categoryLabel handles human readable names
    .join("");

  return `
    <article class="card equip-card">
      <div class="thumb" aria-hidden="true">${thumbHtml}</div>
      <div class="equip-meta">
        <h3>${name}</h3>
        <div class="cat-tags">${tagsHtml}</div>
      </div>
      <a class="btn btn-small" href="equipment-detail.html?id=${encodeURIComponent(item.id)}">‡¶¨‡¶ø‡¶∏‡ßç‡¶§‡¶æ‡¶∞‡¶ø‡¶§</a>
    </article>
  `;
}

async function initEquipmentLibrary() {
  const grid = qs("#equipGrid");
  if (!grid) return;

  const emptyBox = qs("#equipEmpty");
  const searchInput = qs("#equipSearch");
  const sortSel = qs("#equipSort");
  const chips = qsa(".chip");

  let all = await loadEquipment();
  let state = { q: "", cat: "all", sort: "az" };

  // Pre-populate search and category filters from URL parameters. This makes
  // it possible to link directly to a filtered view (e.g., equipment.html?cat=physics&query=beaker).
  {
    const url = new URL(location.href);
    const qParam = url.searchParams.get('query');
    const catParam = url.searchParams.get('cat');
    if (qParam) {
      // decode encoded spaces and assign to state and input
      state.q = decodeURIComponent(qParam);
      if (searchInput) searchInput.value = state.q;
    }
    if (catParam) {
      state.cat = catParam;
      // update chip active state
      chips.forEach(x => {
        if (x.dataset.cat === catParam) {
          x.classList.add('active');
        } else {
          x.classList.remove('active');
        }
      });
    }
  }

  function apply() {
    const q = state.q.trim().toLowerCase();
    let filtered = all.filter(it => {
      // Determine if this item should appear given the selected category.
      // When state.cat is 'all' include everything. Items may define a
      // 'categories' array or a legacy 'category' string.
      const cats = it.categories || (it.category ? [it.category] : []);
      const inCat = (state.cat === "all") || cats.includes(state.cat);
      if (!inCat) return false;
      // If no search term, include the item
      if (!q) return true;
      // Combine searchable fields into one string for case‚Äëinsensitive search
      const hay = [
        it.name_bn,
        it.name_en,
        it.definition_bn,
        ...(it.aliases_bn || [])
      ].join(" ").toLowerCase();
      return hay.includes(q);
    });

    filtered.sort((a, b) => (a.name_en || "").localeCompare(b.name_en || ""));

    grid.innerHTML = filtered.map(renderCard).join("");
    emptyBox.classList.toggle("hidden", filtered.length > 0);
  }

  searchInput.addEventListener("input", (e) => {
    state.q = e.target.value;
    apply();
  });

  sortSel.addEventListener("change", (e) => {
    state.sort = e.target.value;
    apply();
  });

  chips.forEach(ch => {
    ch.addEventListener("click", () => {
      chips.forEach(x => x.classList.remove("active"));
      ch.classList.add("active");
      state.cat = ch.dataset.cat;
      apply();
    });
  });

  apply();
}

